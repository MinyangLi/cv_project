run_davis_all_methods() {
  local images_dir="$DAVIS_IMG_VID_DIR"
  local masks_dir="$DAVIS_MASK_VID_DIR"
  local rose_out="$DAVIS_OUT_ROOT/rose"
  local ee_out="$DAVIS_OUT_ROOT/effecterase"
  local pp_out="$DAVIS_OUT_ROOT/propainter"

  if [[ ! -d "$images_dir" || ! -d "$masks_dir" ]]; then
    echo "[SKIP] DAVIS videos not found: $images_dir and/or $masks_dir"
    echo "       Generate first, e.g.: python ${ROOT}/frames2video.py --root ${DAVIS_ROOT} --overwrite"
    return 0
  fi

  mkdir -p "$rose_out" "$ee_out" "$pp_out"

  local mp4s=()
  mapfile -t mp4s < <(sorted_mp4s_in_dir "$images_dir")
  if [[ ${#mp4s[@]} -eq 0 ]]; then
    echo "[SKIP] DAVIS: no .mp4 under $images_dir"
    return 0
  fi

  local f base _t0 _t1 _dt
  for f in "${mp4s[@]}"; do
    base=$(basename "$f")
    [[ -f "$masks_dir/$base" ]] || { echo "[WARN] DAVIS: missing mask video $masks_dir/$base — skip"; continue; }

    echo "#################### DAVIS: $base ####################"

    echo "========== ROSE  [DAVIS]  $base =========="
    _t0=$(date +%s)
    (
      set +u
      cd "$ROSE_DIR"
      conda activate rose
      python inference.py \
        --validation_videos "$f" \
        --validation_masks "$masks_dir/$base" \
        --validation_prompts "" \
        --output_dir "$rose_out"
    )
    _t1=$(date +%s)
    _dt=$((_t1 - _t0))
    ROSE_SEC+=_dt
    ROSE_VIDEOS+=1
    echo "[TIME] ROSE [DAVIS] $base ${_dt}s"

    echo "========== EffectErase  [DAVIS]  $base =========="
    _t0=$(date +%s)
    (
      set +u
      cd "$EE_DIR"
      conda activate effecterase
      CUDA_VISIBLE_DEVICES="${CUDA_VISIBLE_DEVICES:-0}" python examples/remove_wan/infer_remove_wan.py \
        --fg_bg_path "$f" \
        --mask_path "$masks_dir/$base" \
        --output_path "$ee_out/$base" \
        --text_encoder_path "$TEXT_ENC" \
        --vae_path "$VAE_P" \
        --dit_path "$DIT_P" \
        --image_encoder_path "$IMG_ENC" \
        --pretrained_lora_path "$LORA_P"
    )
    _t1=$(date +%s)
    _dt=$((_t1 - _t0))
    EE_SEC+=_dt
    EE_VIDEOS+=1
    echo "[TIME] EffectErase [DAVIS] $base ${_dt}s"

    echo "========== ProPainter  [DAVIS]  $base =========="
    _t0=$(date +%s)
    (
      set +u
      cd "$PP_DIR"
      conda activate propainter
      CUDA_VISIBLE_DEVICES="${CUDA_VISIBLE_DEVICES:-0}" python inference_propainter.py \
        --video "$f" \
        --mask "$masks_dir/$base" \
        --output "$pp_out"
    )
    _t1=$(date +%s)
    _dt=$((_t1 - _t0))
    PP_SEC+=_dt
    PP_VIDEOS+=1
    echo "[TIME] ProPainter [DAVIS] $base ${_dt}s"
  done
}

if [[ "$RUN_BENCH" == "1" ]]; then
  # Categories: direct children of Benchmark that have both Unedited/ and Masked/
  CATS=()
  for d in "$BENCH"/*/; do
    [[ -d "$d" ]] || continue
    c=$(basename "$d")
    [[ -d "$BENCH/$c/Unedited" && -d "$BENCH/$c/Masked" ]] || continue
    CATS+=("$c")
  done
  if [[ ${#CATS[@]} -eq 0 ]]; then
    echo "[ERROR] No category folders with Unedited+Masked under $BENCH"
    exit 1
  fi
fi

declare -i WALL_START WALL_END
WALL_START=$(date +%s)

if [[ "$RUN_BENCH" == "1" ]]; then
  for cat in "${CATS[@]}"; do
    [[ -d "$BENCH/$cat/Unedited" && -d "$BENCH/$cat/Masked" ]] || continue
    echo "#################### Category: $cat ####################"
    run_rose_category "$cat"
    run_effecterase_category "$cat"
    run_propainter_category "$cat"
  done
else
  echo "[SKIP] Benchmark disabled (RUN_BENCH=$RUN_BENCH)"
fi

if [[ "$RUN_DAVIS" == "1" ]]; then
  echo "#################### DAVIS ####################"
  run_davis_all_methods
else
  echo "[SKIP] DAVIS disabled (RUN_DAVIS=$RUN_DAVIS)"
fi

WALL_END=$(date +%s)
declare -i WALL_SEC=$((WALL_END - WALL_START))

echo ""
echo "================================================================================"
echo "  INFERENCE_ALL  SUMMARY"
echo "================================================================================"
echo "  ROSE          videos: ${ROSE_VIDEOS}   total: ${ROSE_SEC}s  ($(fmt_hms "${ROSE_SEC}"))   avg/video: $(avg_sec "${ROSE_SEC}" "${ROSE_VIDEOS}")s"
echo "  EffectErase   videos: ${EE_VIDEOS}    total: ${EE_SEC}s   ($(fmt_hms "${EE_SEC}"))   avg/video: $(avg_sec "${EE_SEC}" "${EE_VIDEOS}")s"
echo "  ProPainter    videos: ${PP_VIDEOS}   total: ${PP_SEC}s   ($(fmt_hms "${PP_SEC}"))   avg/video: $(avg_sec "${PP_SEC}" "${PP_VIDEOS}")s"
echo "  ---"
echo "  Wall-clock (entire script): ${WALL_SEC}s  ($(fmt_hms "${WALL_SEC}"))"
echo "================================================================================"
echo "[DONE] All categories processed under $BENCH"
